package edu.matheus.secondweek;

public class MyClassTwo {
    public static void main(String[] args) {
        float a = 10;
        float b = 5;

        sum(a, b);
        sub(a, b);
    }

    public static void sum(float a, float b) {
        System.out.println(a + b);
    }

    public static void sub(float a, float b) {
        System.out.println(a - b);
    }
}
